document.getElementById("contactForm").addEventListener("submit", function (e) {
    let hasError = false;

    // Regex patterns
    const namePattern = /^[A-Za-z\s]+$/;
    const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    const textPattern = /^[A-Za-z\s.,!?'"-]+$/;

    // Fields
    let nameInput = document.getElementById("name");
    let emailInput = document.getElementById("email");
    let subInput = document.getElementById("sub");
    let messageInput = document.getElementById("message");

    // Clear old errors
    document.querySelectorAll(".error-message").forEach(msg => msg.style.display = "none");

    // Name validation
    if (!nameInput.value.trim()) {
        nameInput.parentElement.querySelector(".error-message").textContent = "Name is required";
        nameInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    } else if (!namePattern.test(nameInput.value.trim())) {
        nameInput.parentElement.querySelector(".error-message").textContent = "Name must contain only letters";
        nameInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    }

    // Email validation
    if (!emailInput.value.trim()) {
        emailInput.parentElement.querySelector(".error-message").textContent = "Email is required";
        emailInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    } else if (!emailPattern.test(emailInput.value.trim())) {
        emailInput.parentElement.querySelector(".error-message").textContent = "Enter a valid email";
        emailInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    }

    // Subject validation
    let subVal = subInput.value.trim();

    function countSentences(text) {
      return text.split(/[.!?]+/).filter(sentence => sentence.trim().length > 10).length;
    }

    if (!subVal) {
        subInput.parentElement.querySelector(".error-message").textContent = "Subject is required";
        subInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    } else if (countSentences(subVal) < 1) {
        subInput.parentElement.querySelector(".error-message").textContent = "Subject must contain at least 1 sentences";
        subInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    } else if (!namePattern.test(subVal.replace(/[.!?]/g, '').trim())) {
        // Remove punctuation and test letters only
        subInput.parentElement.querySelector(".error-message").textContent = "Subject must contain only letters";
        subInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    }

    // Message validation
    let msgVal = messageInput.value.trim();
    if (!msgVal) {
        messageInput.parentElement.querySelector(".error-message").textContent = "Message is required";
        messageInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    } else if (msgVal.length < 10) {
        messageInput.parentElement.querySelector(".error-message").textContent = "Message must be at least 10 characters";
        messageInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    } else if (!textPattern.test(msgVal)) {
        messageInput.parentElement.querySelector(".error-message").textContent = "Message must contain letters (no pure numbers)";
        messageInput.parentElement.querySelector(".error-message").style.display = "block";
        hasError = true;
    }

    // Shake animation if error
    if (hasError) {
        e.preventDefault();
        this.classList.remove("shake-div");
        void this.offsetWidth; // restart animation
        this.classList.add("shake-div");
    }
});
</script>


